namespace bnetlib\Resource\Entity\Wow\Guild;

use bnetlib\Resource\Entity\EntityInterface;
use bnetlib\ServiceLocator\ServiceLocatorInterface;

/**
 * @category   bnetlib
 * @package    Resource
 * @subpackage WorldOfWarcraft
 * @copyright  2012 Eric Boh <cossish@gmail.com>
 * @license    http://coss.gitbub.com/bnetlib/license.html    MIT License
 */
class Member implements EntityInterface
{
    /**
     * @var array
     */
    protected $data = array();

    /**
     * @var array|null
     */
    protected $headers;

    /**
     * @var ServiceLocatorInterface
     */
    protected $serviceLocator;

    /**
     * @inheritdoc
     */
    public function populate($data)
    {
        $class = $this->serviceLocator->get('wow.entity.character');
        if (isset($this->headers)) {
            $class->setResponseHeaders($this->headers);
        }
        $class->populate($data['character']);

        $this->data['character'] = $class;
        $this->data['rank']      = $data['rank'];
    }

    /**
     * @inheritdoc
     */
    public function getResponseHeaders()
    {
        return $this->headers;
    }

    /**
     * @inheritdoc
     */
    public function setResponseHeaders($headers)
    {
        $this->headers = $headers;
    }

    /**
     * @inheritdoc
     */
    public function setServiceLocator(ServiceLocatorInterface $locator)
    {
        $this->serviceLocator = $locator;
    }

    /**
     * @return integer
     */
    public function getRank()
    {
        return $this->data['rank'];
    }

    /**
     * @return \bnetlib\Resource\Entity\Wow\Character
     */
    public function getCharacter()
    {
        return $this->data['character'];
    }
}