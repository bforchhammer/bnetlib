class Follower implements EntityInterface, \IteratorAggregate
{
    /**
     * @var integer
     */
    protected $position = 0;

    /**
     * @var array
     */
    protected $index = array();

    /**
     * @var array
     */
    protected $data = array();

    /**
     * @var \stdClass|null
     */
    protected $headers;

    /**
     * @var ServiceLocatorInterface
     */
    protected $serviceLocator;

    /**
     * @inheritdoc
     */
    public function populate($data)
    {
        $this->data = $data;

        $this->data['skills'] = $this->serviceLocator->get('d3.entity.follower.skills');
        if (isset($this->headers)) {
            $this->data[$key]->setResponseHeaders($this->headers);
        }
        $this->data['skills']->populate($data['skills']);
    }


    /**
     * @inheritdoc
     */
    public function getResponseHeaders()
    {
        return $this->headers;
    }

    /**
     * @inheritdoc
     */
    public function setResponseHeaders($headers)
    {
        $this->headers = $headers;
    }

    /**
     * @inheritdoc
     */
    public function setServiceLocator(ServiceLocatorInterface $locator)
    {
        $this->serviceLocator = $locator;
    }

    /**
     * @return string
     */
    public function getSlug()
    {
        return $this->data['slug'];
    }

    /**
     * @return string
     */
    public function getName()
    {
        return $this->data['name'];
    }

    /**
     * @return string
     */
    public function getRealName()
    {
        return $this->data['realName'];
    }

    /**
     * @return string
     */
    public function getPortrait()
    {
        return $this->data['portrait'];
    }

    /**
     * @return Follower\Skills
     */
    public function getSkills()
    {
        return $this->data['skills'];
    }

    /**
     * @see    \IteratorAggregate
     * @return Follower\Skills
     */
    public function getIterator()
    {
        return $this->data['skills'];
    }

}